# If Condition
a = 10
b = 15
if b> a:
    print("b lebih besar dari a")
    
# Elif Condition
a = 10
b = 10
if b > a:
    print("b lebih besar dari a")
elif a == b:
    print("a sama dengan b")
    
# Else
a = 100
b = 10
if b> a:
    print("b lebih besar dari a")
elif a == b:
    print("a sama dengan b")
else:
    print("a lebih besar dari b")
    
